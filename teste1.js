// Declarando as variáveis
let petDog = "Rex";
let petCat = "Pepper";

// Registrando as variáveis no console
console.log(petDog);
console.log(petCat);

// Registrando os textos e variáveis no console
console.log("My pet dog's name is: " + petDog);
console.log("My pet cat's name is: " + petCat);

// Declarando variáveis adicionais para os sons
let catSound = "purr";
let dogSound = "woof";

// Registrando os sons no console
console.log(petDog + " says " + dogSound);
console.log(petCat + " says " + catSound);

// Reatribuindo o valor de catSound
catSound = "meow";

// Registrando a mudança no console
console.log(petCat + " now says " + catSound);
